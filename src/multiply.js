/**
 * Multiplies three numbers together.
 * @param {number} a - First number
 * @param {number} b - Second number
 * @param {number} c - Third number
 * @returns {number} The product of a, b, and c
 */
function multiplyThree(a, b, c) {
  return a * b * c;
}

// Example usage
const result = multiplyThree(2, 3, 4);
console.log(`2 × 3 × 4 = ${result}`); // 24

export { multiplyThree };
